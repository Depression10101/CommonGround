// Function to initialize default reviews if they don't exist
export function initializeDefaultReviews() {
  const existingReviews = localStorage.getItem('reviews');
  
  if (!existingReviews) {
    localStorage.setItem('reviews', JSON.stringify(defaultReviews));
  } else {
    // Merge with existing reviews
    const reviews = JSON.parse(existingReviews);
    const reviewIds = reviews.map((r: any) => r.id);
    
    const newReviews = defaultReviews.filter(dr => !reviewIds.includes(dr.id));
    
    if (newReviews.length > 0) {
      localStorage.setItem('reviews', JSON.stringify([...reviews, ...newReviews]));
    }
  }
}
